export function getRandomNicknames():string[]{
  const adjIdx = Math.floor(Math.random() * 34);
  const nounIdx = Math.floor(Math.random() * 45);
  const randNum = Math.floor(Math.random() * 8999)+1000;
  const nickname : string[] = [adjectives[adjIdx]+nouns[nounIdx]+randNum.toString()];
  for(let i = 0; i < 10; ++i) {
    nickname.push(adjectives[adjIdx] + nouns[nounIdx] + randNum.toString());
  }
  return nickname;
}